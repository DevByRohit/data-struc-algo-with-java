public class A_If_Else {
    public static void main(String[] args) {
        // If-Else Statements
        boolean condition = true;
        // 1. Syntax:
        if (condition) {
            // code to be executed
        } else {
            // code to be executed
        }

        // 2. if:- Executes block if condition is true, otherwise skip execution.
        // 3. else:- Executes block if condition is false.
        // 4. Curly Braces can be ommited for single statement, but not recommended.
        // 5. if-else Ladder:- Multiple if and else-if blocks, Only one executes.

        // Example:
        if (condition) {
            System.out.println("I will run when condition is true");
        } else {
            System.out.println("I will run when condition is false");
        }
    }
}
